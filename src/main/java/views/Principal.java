    private void llenadoPanel() {
        try {
            JPanel pContenedor = new JPanel();
            pContenedor.setLayout(new GridLayout(3, 3, 10, 10));
            System.out.println("LlenandoPanel de pokemon>> pagina: " + page + ", base: " + base + " maximo: " + max);
            for (int i = (max - base); i < max; i++) {
                CardPokemon cardPoke = new CardPokemon(listaPokemon.get(i));
                pContenedor.add(cardPoke);
            }
            this.Contenedor.setViewportView(pContenedor);
        } catch (Exception e) {
            System.out.println(e);
        }
    }

    private void buscarPokemon() {
        try {
            JPanel pContenedor = new JPanel();
            int filas = base < 3 ? 1 : 3;
            System.out.println("Buscando pokemon>> pagina: " + page + ", base: " + base + " maximo: " + max);
            pContenedor.setLayout(new GridLayout(filas, 3, 10, 10));
            for (int i = (max - base); i < max; i++) {
                CardPokemon cardPoke = new CardPokemon(listaPokemonAx.get(i));
                pContenedor.add(cardPoke);
            }
            this.Contenedor.setViewportView(pContenedor);
        } catch (Exception e) {
            System.out.println(e);
        }
    }

