    private void btnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguienteActionPerformed
        page++;
        max = page * 9;

        if (max > listaPokemon.size() && !flag) {
            max = listaPokemon.size();
            page--;
        }

        if (max > listaPokemonAx.size() && flag) {
            max = listaPokemonAx.size();
            page--;
        }

        if (flag) {
            buscarPokemon();
        } else {
            llenadoPanel();
        }
        this.lbPage.setText("Page. " + page);
    }//GEN-LAST:event_btnSiguienteActionPerformed

    private void btnAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnteriorActionPerformed
        page--;
        max = page * 9;
        if (max < 1) {
            max = 9;
            page++;
        }
        if (listaPokemon.size() < 9 && !flag) {
            max = listaPokemon.size();
            page = 1;
        }

        if (listaPokemonAx.size() < 9 && flag) {
            max = listaPokemonAx.size();
            page = 1;
        }

        if (flag) {
            buscarPokemon();
        } else {
            llenadoPanel();
        }
        this.lbPage.setText("Page. " + page);
    }//GEN-LAST:event_btnAnteriorActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        page = 1;
        max = 9;
        base = 9;
        String txt = this.txtSearch.getText();
        if (txt.length() < 1) {
            flag = false;
            llenadoPanel();
        } else if (txt.length() >= 1) {
            flag = true;
            this.listaPokemonAx = new ArrayList<Pokemon>();
            for (Pokemon pk : this.listaPokemon) {
                if (this.jFiltro.getSelectedItem().toString().equals("Nombre") && pk.getName().toUpperCase().contains(txt.toUpperCase())) {
                    this.listaPokemonAx.add(pk);
                }
                if (this.jFiltro.getSelectedItem().toString().equals("Color") && pk.getColor().toUpperCase().equals(txt.toUpperCase())) {
                    this.listaPokemonAx.add(pk);
                }
                if (this.jFiltro.getSelectedItem().toString().equals("Habitat") && pk.getHabitat().toUpperCase().equals(txt.toUpperCase())) {
                    this.listaPokemonAx.add(pk);
                }
                if (this.jFiltro.getSelectedItem().toString().equals("Rate de Captura") && pk.getCapture_rate().toUpperCase().equals(txt.toUpperCase())) {
                    this.listaPokemonAx.add(pk);
                }
                if (this.jFiltro.getSelectedItem().toString().equals("Experiencia") && pk.getBase_experience().toUpperCase().equals(txt.toUpperCase())) {
                    this.listaPokemonAx.add(pk);
                }
                if (this.jFiltro.getSelectedItem().toString().equals("Especie") && pk.getSpecies().toUpperCase().equals(txt.toUpperCase())) {
                    this.listaPokemonAx.add(pk);
                }

            }
            if (listaPokemonAx.size() < 9) {
                base = listaPokemonAx.size();
                max = listaPokemonAx.size();
            }
            buscarPokemon();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

